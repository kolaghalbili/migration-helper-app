  static List<String> searchLocations(String query) {
    if (query.trim().isEmpty) return [];
    final q = query.toLowerCase();

    final cityResults = cities
        .where((c) =>
            c['name']!.toLowerCase().contains(q) ||
            c['country']!.toLowerCase().contains(q))
        .map((c) => '${c['name']}, ${c['country']}')
        .toList();

    final countryResults = countries
        .where((c) => c.toLowerCase().contains(q))
        .toList();

    final combined = <String>{...cityResults, ...countryResults}.toList();
    combined.sort((a, b) {
      final aStarts = a.toLowerCase().startsWith(q) ? 0 : 1;
      final bStarts = b.toLowerCase().startsWith(q) ? 0 : 1;
      if (aStarts != bStarts) return aStarts - bStarts;
      return a.compareTo(b);
    });
    return combined.take(10).toList();
  }

  static List<String> searchCountries(String query) {
    if (query.trim().isEmpty) return [];
    final q = query.toLowerCase();
    return countries
        .where((c) => c.toLowerCase().contains(q))
        .take(8)
        .toList();
  }

  static List<String> searchLanguages(String query) {
    if (query.trim().isEmpty) return [];
    final q = query.toLowerCase();
    return languages
        .where((l) => l.toLowerCase().contains(q))
        .take(8)
        .toList();
  }
